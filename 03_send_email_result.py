def read_report(filepath):
    """
    Reads the plain text report from Script 2.
    Returns the content as a string, or raises an error if file not found.
    """
    if not os.path.exists(filepath):
        raise FileNotFoundError(
            f"'{filepath}' not found. Please run 02_analyse_with_ai.py first."
        )

    with open(filepath, "r") as f:
        return f.read()


