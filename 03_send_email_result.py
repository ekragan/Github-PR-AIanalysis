def build_email(sender, recipient, cc_list, subject, plain_text, html_body):
    """
    Builds the email message object with both plain text and HTML versions.

    WHY SEND BOTH PLAIN TEXT AND HTML?
    This is called a "multipart/alternative" email — a best practice in email sending.
    Email clients choose which version to display:
      - Modern clients (Gmail, Outlook) → show the HTML version
      - Older clients or plain-text mode → fall back to plain text
    Always include both so nobody gets a broken email.

    HOW MIMEMultipart WORKS:
      - MIMEMultipart("alternative") = a container that holds multiple versions of the same content
      - We attach plain text first, HTML second
      - Email clients always pick the LAST attached part they can render (so HTML wins)
    """

    # Create the container email object
    msg = MIMEMultipart("alternative")
    msg["Subject"] = subject
    msg["From"]    = sender
    msg["To"]      = recipient

    # Add CC recipients if any were specified
    if cc_list:
        msg["Cc"] = ", ".join(cc_list)

    # Attach plain text version (fallback for old email clients)
    part_plain = MIMEText(plain_text, "plain")
    msg.attach(part_plain)

    # Attach HTML version (shown by modern email clients)
    part_html = MIMEText(html_body, "html")
    msg.attach(part_html)

    return msg


def send_email(sender, app_password, recipient, cc_list, message):
    """
    Connects to Gmail's SMTP server and sends the email.

    HOW SMTP WORKS:
      SMTP (Simple Mail Transfer Protocol) is the standard for sending email.
      Think of it like a post office:
        1. You connect to the post office (smtp.gmail.com on port 587)
        2. You identify yourself (ehlo)
        3. You secure the connection (starttls — encrypts so password isn't sent in plain text)
        4. You log in with your credentials
        5. You hand over the letter (sendmail)
        6. You leave (quit)

    PORT 587:
      Gmail supports two ports for sending email:
        - Port 465 : SSL from the start (older method)
        - Port 587 : STARTTLS — starts plain then upgrades to encrypted (recommended)
      We use 587 + starttls() which is the modern standard.
    """

    # Collect all recipient addresses (To + CC) for the actual sending
    # The envelope (who actually receives it) is separate from the message headers
    all_recipients = [recipient] + cc_list

    print(f"  Connecting to Gmail SMTP server...")

    # smtplib.SMTP() opens a connection to the mail server
    # 'with' ensures the connection is closed automatically when done
    with smtplib.SMTP("smtp.gmail.com", 587) as server:

        server.ehlo()       # identify ourselves to the server (required handshake)
        server.starttls()   # upgrade connection to encrypted (TLS) — always do this before login
        server.ehlo()       # re-identify after upgrading to TLS

        print(f"  Logging in as {sender}...")
        server.login(sender, app_password)  # authenticate with App Password

        print(f"  Sending email to {recipient}...")
        server.sendmail(
            sender,           # from address
            all_recipients,   # list of all recipients
            message.as_string()  # the full email as a formatted string
        )

    print(f"  Email sent successfully!")


